        <h3>緣起 </h3>

        <p>
       Fab Lab 亞洲年會此一活動的目的，在於藉由 Fab Lab 亞洲年會、工 作坊、研討會、創意競賽及國際論壇等系列活動，
       和亞洲及大洋洲區域 Fab Lab 間分享交流，一方面加強亞洲區 Fab Lab 間直接的溝通，促使各 個 Fab Lab 間交流彼此營運及技術上的經驗，
       相互學習，同時也藉由工作 坊、研討會來討論、分享 Fab Lab 社群未來發展的方向及促成更多國際合 作的計畫。

       此次年會更將與 Maker Faire Taipei 結合，藉由實際參與 Maker Faire Taipei 的展出，國際論壇的舉辦及媒體報導，
       讓大眾看到亞洲 各地不同 Fab Lab 的創意，也讓大眾更明瞭 Fab Lab 國際組織的精神及數 位化個人自造的特性，
       以及全球設計，在地製造，創意產出得以自由流通 的分享理念。 </p>

        <h3>什麼是Fab Lab</h3>

        <p> Fab Lab 是 Fabrication Laboratory 的簡稱，
          是個推廣數位製造工具以 促進新發明的國際性非營利組織，
          目前在全世界八十多個國家已經有超過 四百三十多家這樣的機構。

          最初的概念是由美國麻省理工學院（MIT）的分子 與原子中心（Center of Bits and Atoms）Neil Gershenfeld 教授所提出。

          他相信藉由推廣數位製造機具的使用及加強知識分享、國際交流、國際合作，
          能 夠達到加速創新應用的發展及科技進步的目的。
        </p>

        <h3>Fab Lab的運用</h3>

        <p>
          Fab Lab 為世界所帶來的改變不僅是應用數位製造機具的使用，而是 透過「數位製造」協助在地的發展，
          例如：非洲因為醫療設備的短缺，造成 孕婦死亡率極高，但是應用數位製造的方式，
          就地生產便宜的器具，以協助 孕婦生產等，對當地的醫療改進造成相當大的提升，
          這是數位製造當初極 力促成對落後及偏遠地區的協助。 
        </p>
        <p>
          冰島透過 Fab Lab 組織提供青年創業的環境，讓冰島在金融海嘯之後 能夠透過數位製造的機制，
          協助青年發揮創意，促進經濟的快速發展。
          目前 冰島有六個 Fab Lab，提供在地的居民進行創意的實現與創業的發展，
          這個層次已經不是學習數位機具的操作，而是真實的應用到生存的必須，
          為此創造價值與產值，這也值得其他國家積極學習。 
        </p>
        <p>
          西班牙巴塞隆納面對 Internet 來臨的世代，何因應虛擬及數位所帶來 的改變，成為都市持續發展的重要議題。
          
          目前巴塞隆納已經有相當多的 Fab Lab 可供使用，
          包括與建築學院 IAAC 合作的 FabLab Barcelona，
          經營超過兩年的 Maker of Barcelona 及其他重要的 Maker Space。


          未來，市政府期望每個行政區建構一個類似 Fab Lab 的數位製造實驗室，
          讓當地的 居民可以就近利用此一服務，從事數位機具的應用、專案的創作及創業的 發展，

          最終期使讓巴塞隆納成為一座 Fab City，這是為城市發展帶來轉變 。 
        </p>

        <h3>Fab Lab在台灣</h3>

        <p>
          2013 年五月，Fablab Taipei 開始營運，為台灣第一家 Fab Lab。在一 年多的過程中，成功了打造了一個相當活躍的台北自造者社群，
          也在今年二月經由內政部核准設立為台灣自造者協會，並在今年四月間招開成立大會。
          Fablab Taipei 的目標是打造一個台北 Maker 的天堂，並由社群自主營運， 基於推廣及教育的目的，在台灣推動 Make、Learn、Share 的 Fab Lab 核心精神。 
        </p>
        <p>
          台北數位藝術中心於 2013 年九月創立 FabLab Dynamic，以社會設計 （Social Design）為發展核心方向，希望透過數位製造技術，針對社會需求而提 出改善、改造的創新專案，發展在地特色。
        </p>
        <p> 
          Fablab Tainan 由台灣數位文化協會於 2013 年十二月成立，為南台灣 第一家 Fab Lab，是一個充分與在地文化結合的 Maker 空間。以市民工坊 出發，提供在地數位文創工作者一個交流育成的平臺，將產業聚落融入地 方經濟、文化與市民生活。 
        </p>


